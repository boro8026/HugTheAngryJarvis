/**
 * This enum will hold the different possible directions that the
 * stylus can move throughout the board.
 * 
 * @author Brandon Borowiak
 */
public enum Direction 
{
	/**
	 * This enumeration will be used to designate a movement up
	 */
	UP,
	/**
	 * This enumeration will be used to designate a movement down
	 */
	DOWN,
	/**
	 * This enumeration will be used to designate a movement left
	 */
	LEFT,
	/**
	 * This enumeration will be used to designate a movement right
	 */
	RIGHT,
	/**
	 * This enumeration will be used to designate a movement up and left
	 */
	UP_LEFT,
	/**
	 * This enumeration will be used to designate a movement up and right
	 */
	UP_RIGHT,
	/**
	 * This enumeration will be used to designate a movement down and left
	 */
	DOWN_LEFT,
	/**
	 * This enumeration will be used to designate a movement down and right
	 */
	DOWN_RIGHT;
}//Direction
